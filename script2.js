        // Elemen DOM
        const gridBuku = document.getElementById('grid-buku');
        const inputPencarian = document.getElementById('input-pencarian');
        const tombolPencarian = document.getElementById('tombol-pencarian');
        const filterKategori = document.getElementById('filter-kategori');
        const filterKelas = document.getElementById('filter-kelas');
        const modalBuku = document.getElementById('modal-buku');
        const detailBuku = document.getElementById('detail-buku');
        const tutupModal = document.querySelector('.tutup-modal');
        const menuMobile = document.querySelector('.menu-mobile');
        const menuNav = document.querySelector('nav ul'); 
        const header = document.getElementById('header');
        const tombolGulirKeAtas = document.getElementById('gulir-ke-atas');

        // Fungsi untuk menampilkan buku dengan animasi
        function tampilkanBuku(bukuUntukDitampilkan) {
            gridBuku.innerHTML = '';
            
            if (bukuUntukDitampilkan.length === 0) {
                gridBuku.innerHTML = '<p style="grid-column: 1/-1; text-align: center; padding: 2rem;">Tidak ada buku yang ditemukan.</p>';
                return;
            }
            
            bukuUntukDitampilkan.forEach((buku, indeks) => {
                const kartuBuku = document.createElement('div');
                kartuBuku.className = 'kartu-buku';
                kartuBuku.innerHTML = `
                    <div class="sampul-buku">
                        <img src="${buku.sampul}" alt="${buku.judul}" onerror="this.src='https://source.unsplash.com/random/300x400/?book'">
                    </div>
                    <div class="info-buku">
                        <h3 class="judul-buku">${buku.judul}</h3>
                        <p class="penulis-buku">${buku.penulis}</p>
                        <div class="meta-buku">
                            <span>Kelas ${buku.kelas}</span>
                            <span>${buku.halaman} halaman</span>
                        </div>
                    </div>
                    <button class="tombol-baca" data-id="${buku.id}">Baca Buku</button>
                `;
                gridBuku.appendChild(kartuBuku);
                
                // Animasi muncul berurutan
                setTimeout(() => {
                    kartuBuku.classList.add('tampil');
                }, 100 * indeks);
            });

            //  untuk tombol baca
            document.querySelectorAll('.tombol-baca').forEach(tombol => {
                tombol.addEventListener('click', function(e) {
                    e.preventDefault();
                    const idBuku = parseInt(this.getAttribute('data-id'));
                    tampilkanDetailBuku(idBuku);
                });
            });
        }

        //  untuk menampilkan detail buku
        function tampilkanDetailBuku(idBuku) {
            const bukuDipilih = buku.find(b => b.id === idBuku);
            if (bukuDipilih) {
                detailBuku.innerHTML = `
                    <img src="${bukuDipilih.sampul}" alt="${bukuDipilih.judul}" onerror="this.src='https://source.unsplash.com/random/300x400/?book'">
                    <div class="info-detail-buku">
                        <h3>${bukuDipilih.judul}</h3>
                        <p><strong>Penulis:</strong> ${bukuDipilih.penulis}</p>
                        <p><strong>Kategori:</strong> ${bukuDipilih.kategori}</p>
                        <p><strong>Kelas:</strong> ${bukuDipilih.kelas}</p>
                        <p><strong>Tahun Terbit:</strong> ${bukuDipilih.tahun}</p>
                        <p><strong>Jumlah Halaman:</strong> ${bukuDipilih.halaman}</p>
                        <p><strong>Deskripsi:</strong> ${bukuDipilih.deskripsi}</p>
                    </div>
                    <a href="${bukuDipilih.tautanBaca}" target="_blank" class="tombol-baca-sekarang">Baca Sekarang</a>
                `;
                
                // Animasi modal muncul 
                modalBuku.classList.add('tampil');
                document.body.style.overflow = 'hidden';
            }
        }

        // Fungsi untuk memfilter buku
        function filterBuku() {
            const istilahPencarian = inputPencarian.value.toLowerCase();
            const kategoriTerpilih = filterKategori.value;
            const kelasTerpilih = filterKelas.value;

            const bukuTersaring = buku.filter(buku => {
                const cocokPencarian = buku.judul.toLowerCase().includes(istilahPencarian) || 
                                     buku.penulis.toLowerCase().includes(istilahPencarian) ||
                                     buku.kategori.toLowerCase().includes(istilahPencarian);
                const cocokKategori = kategoriTerpilih ? buku.kategori === kategoriTerpilih : true;
                const cocokKelas = kelasTerpilih ? buku.kelas === kelasTerpilih : true;
                
                return cocokPencarian && cocokKategori && cocokKelas;
            });
            
            tampilkanBuku(bukuTersaring);
        }

        // Fungsi untuk animasi gulir
        function aturGulir() {
            // Efek penyusutan header
            if (window.scrollY > 100) {
                header.classList.add('header-tergulir');
            } else {
                header.classList.remove('header-tergulir');
            }
            
            // Visibilitas tombol gulir ke atas
            if (window.scrollY > 300) {
                tombolGulirKeAtas.classList.add('tampil');
            } else {
                tombolGulirKeAtas.classList.remove('tampil');
            }
            
            // Animasi untuk elemen yang muncul saat digulir
            const kartuBuku = document.querySelectorAll('.kartu-buku:not(.tampil)');
            const kartuKategori = document.querySelectorAll('.kartu-kategori:not(.tampil)');
            
            kartuBuku.forEach(kartu => {
                const posisiAtasKartu = kartu.getBoundingClientRect().top;
                const tinggiJendela = window.innerHeight;
                
                if (posisiAtasKartu < tinggiJendela - 100) {
                    kartu.classList.add('tampil');
                }
            });
            
            kartuKategori.forEach(kartu => {
                const posisiAtasKartu = kartu.getBoundingClientRect().top;
                const tinggiJendela = window.innerHeight;
                
                if (posisiAtasKartu < tinggiJendela - 100) {
                    kartu.classList.add('tampil');
                }
            });
        }

        // Pendengar Acara
        tombolPencarian.addEventListener('click', filterBuku);
        inputPencarian.addEventListener('keyup', function(e) {
            if (e.key === 'Enter') {
                filterBuku();
            }
        });
        filterKategori.addEventListener('change', filterBuku);
        filterKelas.addEventListener('change', filterBuku);

        // Pendengar acara untuk kategori
        document.querySelectorAll('.kartu-kategori').forEach(kartu => {
            kartu.addEventListener('click', function() {
                const kategori = this.getAttribute('data-kategori');
                filterKategori.value = kategori;
                filterBuku();
                
                // Gulir ke bagian buku
                document.getElementById('books').scrollIntoView({ behavior: 'smooth' });
            });
        });

        // Pendengar acara untuk modal 
        tutupModal.addEventListener('click', function() {
            modalBuku.classList.remove('tampil');
            document.body.style.overflow = 'auto';
        });

        window.addEventListener('click', function(e) {
            if (e.target === modalBuku) {
                modalBuku.classList.remove('tampil');
                document.body.style.overflow = 'auto';
            }
        });

        // Pendengar acara untuk menu mobile 
        menuMobile.addEventListener('click', function() {
            menuNav.classList.toggle('tampil');
            // Ganti ikon menu
            const ikon = this.querySelector('i');
            if (ikon.classList.contains('fa-bars')) {
                ikon.classList.replace('fa-bars', 'fa-times');
            } else {
                ikon.classList.replace('fa-times', 'fa-bars');
            }
        });

        // Pendengar acara untuk gulir ke atas
        tombolGulirKeAtas.addEventListener('click', function() {
            window.scrollTo({
                top: 0,
                behavior: 'smooth'
            });
        });

        // Pendengar acara untuk gulir
        window.addEventListener('scroll', aturGulir);

        //  tampilkan semua buku saat halaman dimuat
        window.addEventListener('DOMContentLoaded', function() {
            tampilkanBuku(buku);
            
            // Animasi untuk kartu kategori
            setTimeout(() => {
                document.querySelectorAll('.kartu-kategori').forEach((kartu, indeks) => {
                    setTimeout(() => {
                        kartu.classList.add('tampil');
                    }, 150 * indeks);
                });
            }, 500);
        });
